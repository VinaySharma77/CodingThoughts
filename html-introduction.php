    <h2>HTML Code Example</h2>
    <p class="para code">
        &lt;&#33;DOCTYPE html&gt;<br>
        &lt;html&gt;<br>
            &lt;head&gt;<br>
                &lt;title&gt;My HTML Document&lt;&#47;title&gt;<br>
                &lt;body&gt;<br>
                    &lt;h1&gt;HTML Demo Heading&lt;&#47;h1&gt;<br>
                    &lt;p&gt;HTML Demo Paragraph&lt;&#47;p&gt;<br>
                &lt;&#47;body&gt;<br>
            &lt;&#47;head&gt;<br>
        &lt;&#47;html&gt;
    </p>
    <p class="para">
        Here is the explanation of the above html code :-<br>
        &lt;&#33;DOCTYPE html&gt; - It defines this document is HTML5 document.<br>
        &lt;html&gt; - It is root element of the html document.<br>
        &lt;head&gt; - It contais the meta information (Meta Data). Meta data is information about data.<br>
        &lt;title&gt; - It displays the title of the html document. We can see it on browser's title bar.<br>
        &lt;body&gt; - It displays the content of the html on browser's screen.<br>
        &lt;h1&gt; - It is used to create heading for the document.<br>
        &lt;p&gt; - It is used to create paragraph for the document.
    </p>
