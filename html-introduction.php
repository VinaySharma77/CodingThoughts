    <h1 class="head">HTML Introduction</h1>
    <p class="para">HTML stands for HyperText Markup Language and it was developed by Tim Berners Lee in 1991. It is used for creating web pages. HTML gives us a structure of the website. It is simply a collection of various elements and tags. HTML elements are important to tell the browser that how to show content on the browser. It contains various elements like headings, paragraphs, links etc. HTML also gives user interface for web applications and mobile applications.
        Markup Language maintains formatting and structure of the document. Web browser translate the html page into readable web pages or audible web pages. Different type of web browsers are used to display the content of the web pages. 
    </p>
