    <h1 class="head">HTML Editors</h1>
    <p class="para">HTML document contains text document. We can create and modify the html document with the use of Text Editors. Various Text Editors are available from basic to advance. Basic editor contains simple user interface but advanced editor may contain professional interface. We can download text editors from the internet or use default text editor that is Notepad for Windows, TextEdit for mac. Professional text editors are Notepad++, Sublime Text, Atom, Visual Studio Code, etc.</p>
    <h2>Create Document with Notepad</h2>
    <p class="para">
        Step 1:-<br>
        First type Notepad on Start Screen that is on bottom left of your computer on windows.
        <br><br>
        Step 2:-<br>
        Type Some HTML or copy from the following.<br>
        <p class="para code">
        &lt;&#33;DOCTYPE html&gt;<br>
        &lt;html&gt;<br>
            &lt;head&gt;<br>
                &lt;title&gt;My HTML Document&lt;&#47;title&gt;<br>
                &lt;body&gt;<br>
                    &lt;h1&gt;HTML Demo Heading&lt;&#47;h1&gt;<br>
                    &lt;p&gt;HTML Demo Paragraph&lt;&#47;p&gt;<br>
                &lt;&#47;body&gt;<br>
            &lt;&#47;head&gt;<br>
        &lt;&#47;html&gt;</p><br><br>
        Step 3:-<br>
        Go to File &gt; Save As in the Notepad. Now save it with any name and give it .html or .htm extension. For example test.html. This extension will create html document.<br><br>
        Step 4:-<br>
        Open HTML document by double clicking the file. Or Right click and click open with option and select any browser to view. 
    </p>
